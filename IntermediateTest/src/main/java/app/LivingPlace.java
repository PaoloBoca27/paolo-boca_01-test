package app;

public class LivingPlace {


	String city;
	String province;
	
	public LivingPlace(String city,String province) {
		this.city=city;
		this.province=province;
	}


	public String getCity() {
		return city;
	}
	public String getProvince() {
		return province;
	}

	public void setCity(String c) {
		this.city=c;
	}
	public void setProvince(String p) {
		this.province=p;
	}
	
	





}




